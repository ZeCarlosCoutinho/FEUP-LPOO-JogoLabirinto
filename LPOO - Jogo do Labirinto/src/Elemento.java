
public class Elemento {
	protected int posx, posy;
	
	public int getPosx() {
		return posx;
	}

	public void setPosx(int posx) {
		this.posx = posx;
	}

	public int getPosy() {
		return posy;
	}

	public void setPosy(int posy) {
		this.posy = posy;
	}

	public Elemento()
	{
		posx = 0;
		posy = 0;
	}
}